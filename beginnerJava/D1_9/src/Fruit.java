
public class Fruit {
	int number;
	String fruitName;
	double price;
	int amount;
	public Fruit(int number, String fruitName, double price, int amount) {
		super();
		this.number = number;
		this.fruitName = fruitName;
		this.price = price;
		this.amount = amount;
	}
	public Fruit() {
		super();
	}
	
}
