package task;

public class Fruit {
	String name;
	String slogan;
	String number;
	int price;
	int amount;
	public Fruit(String name, String slogan, String number, int price, int amount) {
		super();
		this.name = name;
		this.slogan = slogan;
		this.number = number;
		this.price = price;
		this.amount = amount;
	}
	public Fruit() {
		super();
	}	
}
