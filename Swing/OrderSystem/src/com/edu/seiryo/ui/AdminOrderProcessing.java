	private void showTextField(){
		int row = this.jTable1.getSelectedRow();
		dtm = (DefaultTableModel) jTable1.getModel();
		this.jTextField3.setText((String)dtm.getValueAt(row, 3));
		this.jTextField4.setText(String.valueOf(dtm.getValueAt(row, 1)));
		this.jTextField5.setText(String.valueOf(dtm.getValueAt(row, 2)));
		this.jTextField2.setText(String.valueOf(dtm.getValueAt(row, 4)));
	}
	/*
	 * 点击table1显示详细order
	 */
	private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {
		int row = this.jTable1.getSelectedRow();
		if (row == -1) {
			jPane.showMessageDialog(this, "请选择菜品！");
			return;
		}
		dtm = (DefaultTableModel) jTable1.getModel();
		String orderId = (String) dtm.getValueAt(row, 0);
		List<OrdersDetail> list = dService.queryDetail(orderId);
		vData2.clear();
		for (OrdersDetail i : list) {
			Vector vDatail = new Vector();
			vDatail.add(i.getMenu_id());
			vDatail.add(i.getName());
			vDatail.add(i.getAmount());
			vDatail.add(i.getUnit_price());
			vDatail.add(i.getTotal_price());
			vData2.add(vDatail);
		}
		DefaultTableModel model2 = new DefaultTableModel(vData2, vHead2);
		jTable2.setModel(model2);
		showTextField();
	}
	/*
	 * 订单号的模糊查询
	 */
	private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
		vData1.clear();
		String id = this.jTextField1.getText();
		if ("".equals(id)) {
			table1AllShow();
			return;
		}
		List<Order> list = oService.queryById(id);
		if (list.size() != 0) {
			vData1.clear();
			for (Order order : list) {
				Vector vOrder = new Vector();
				vOrder.add(order.getOrder_id());
				vOrder.add(order.getQuantity());
				vOrder.add(order.getTotal_price());
				vOrder.add(order.getOrder_status());
				vData1.add(vOrder);
				this.dtm = new DefaultTableModel(vData1, vHead1);
				this.jTable1.setModel(dtm);
			}
			return;
		}
		this.dtm = new DefaultTableModel(null, vHead1);
		this.jTable1.setModel(dtm);
	}

	/*
	 * 显示table1中所有的order信息
	 */
	private void table1AllShow() {
		List<Order> order = oService.queryOrder();
		this.vHead1 = new Vector();
		this.vData1 = new Vector();
		vHead1.add("订单编号");
		vHead1.add("订单商品数量");
		vHead1.add("订单总金额");
		vHead1.add("订单状态");
		vHead1.add("点餐人");
		for (Order i : order) {
			Vector vOrder = new Vector();
			vOrder.add(i.getOrder_id());
			vOrder.add(i.getQuantity());
			vOrder.add(i.getTotal_price());
			vOrder.add(i.getOrder_status());
			int userId = i.getUserId();
			String userName = userService.queryNameById(userId);
			vOrder.add(userName);
			vData1.add(vOrder);
		}
		this.dtm = new DefaultTableModel(vData1, vHead1);
		this.jTable1.setModel(dtm);
	}
