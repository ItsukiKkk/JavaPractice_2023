	/*
	 * 在未处理状态时取消订单
	 */
	private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
		int row = this.jTable1.getSelectedRow();
		if (row == -1) {
			jPane.showMessageDialog(this, "请选择菜品！");
			return;
		}
		dtm = (DefaultTableModel) jTable1.getModel();
		String status = (String) dtm.getValueAt(row, 3);
		if(!"未处理".equals(status)){
			jPane.showMessageDialog(this, "订单已经处理，取消失败！");
			return;
		}
		String orderId = (String) dtm.getValueAt(row, 0);
		boolean hadDelete = oService.cancelStatus(orderId,"已取消");
		if (hadDelete) {
			jPane.showMessageDialog(this, "取消成功！");
			table1AllShow();
		}
	}
	/*
	 * 在未完成状态时改变订单状态
	 */
	private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {
		int row = this.jTable1.getSelectedRow();
		if (row == -1) {
			jPane.showMessageDialog(this, "请选择菜品！");
			return;
		}
		dtm = (DefaultTableModel) jTable1.getModel();
		String status = (String) dtm.getValueAt(row, 3);
		if ("已完成".equals(status)) {
			jPane.showMessageDialog(this, "订单已完成，操作失败！");
			return;
		}
		String orderId = (String) dtm.getValueAt(row, 0);
		boolean hadDelete = false;
		 if (this.jRadioButton1.isSelected()) {
			  hadDelete = oService.cancelStatus(orderId,"确认订单");
         } else if (this.jRadioButton2.isSelected()) {
        	  hadDelete = oService.cancelStatus(orderId,"去送餐");
         } else if (this.jRadioButton3.isSelected()) {
        	 hadDelete = oService.cancelStatus(orderId,"已完成");
         } else if (this.jRadioButton4.isSelected()) {
        	 dService.deleteByOrderId(orderId);
        	 oService.deleteOrder(orderId);
        	 hadDelete = true;
         }
		if (hadDelete) {
			jPane.showMessageDialog(this, "操作成功！");
			table1AllShow();
			this.jTextField3.setText("");
			this.jTextField4.setText("");
			this.jTextField5.setText("");
		}
	}

